// Type Casting.

#include <iostream>
using namespace std;
int main(){

    int Test1 = 'a';
    cout << "The ASCII value of 'a' is: " << Test1 << endl;

    char Test2 = 98;
    cout << "The character represented by ASCII value 98 is: " << Test2 << endl;

    char Test3 = 123456;
    cout << "The character represented by ASCII value 123456 is: " << Test3 << endl;

    return 0;
}